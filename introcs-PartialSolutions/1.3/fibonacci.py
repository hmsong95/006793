import stdio
import sys

# 명령 줄 인자로 정수 n을 입력받는다.
# 처음 n개의 피보나치 수를 출력한다.

n = int(sys.argv[1])

f = 0
g = 1

for i in range(0, n):
    f = f + g
    g = f - g
    stdio.writeln(f)
    
