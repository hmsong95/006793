import stdio

# 표준 출력 장치에 1000(포함)에서 2000(미포함)까지의 숫자를 한 줄에 5개씩 출력한다.

START = 1000
END = 2000
INTS_PER_LINE = 5

for i in range(START, END):
    stdio.write(str(i) + ' ')
    if i % INTS_PER_LINE == INTS_PER_LINE - 1:
        stdio.writeln()
stdio.writeln()

